        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "gif", "jpg");
        int result = chooser.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File select = chooser.getSelectedFile();
            String path = select.getAbsolutePath();
            mypath.setText(path);
            lbl_image.setIcon(setImage(path));
            Imgpath = path;
        } else {
            JOptionPane.showMessageDialog(null, "لم يتم اختيار أي صورة");
        }

