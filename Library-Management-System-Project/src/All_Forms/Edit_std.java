    private void add_eleveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_eleveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat f = new SimpleDateFormat("YYYY-MM-dd");
        String name = txt_name.getText();
        String Stdtasjil = this.std_tasjil.getText();
        String Date = f.format(txt_birth_date.getDate());
        String birth_place = txt_birth_place.getText();
        String Class = txt_Class.getText();
        String phone = txt_phone.getText();
        String email = txt_email.getText();
        String level = txt_level.getText();
        String Path = mypath.getText();
        conn = getConnection();
        if (name.equals("") || Stdtasjil.equals("") || Class.equals("") || email.equals("") || Date.equals(null) || phone.equals("") || birth_place.equals("") || level.equals("")) {
            JOptionPane.showMessageDialog(null, "الرجاء ملئ جميع الفراغات", "تحذير", 0);
        } else if (Path == null) {
            JOptionPane.showMessageDialog(null, "الرجاء اختيار صورة التلميذ", "تحذير", 0);
        }
        try {
            if (!name.equals("") && !Stdtasjil.equals("") && !birth_place.equals("") && !Class.equals("") && !level.equals("") && !phone.equals("") && !email.equals("") && Path != null) {
                InputStream img = new FileInputStream(new File(Path));
                String update = " update `add_std` set std_name  = ?, std_tasjil = ? , std_birth = ?, std_birth_place = ? , std_class = ? , std_level  = ?, std_phone = ? , std_email = ? , std_img = ? , img_path = ?  where id='" + std_number.getText() + "'";

                preapared = conn.prepareStatement(update);
                preapared.setString(1, name);
                preapared.setString(2, Stdtasjil);
                preapared.setString(3, ((JTextField) txt_birth_date.getDateEditor().getUiComponent()).getText());
                preapared.setString(4, birth_place);
                preapared.setString(5, Class);
                preapared.setString(6, level);
                preapared.setString(7, phone);
                preapared.setString(8, email);
                preapared.setBlob(9, img);
                preapared.setString(10, Path);
                if (preapared.executeUpdate() == 1) {
                    JOptionPane.showMessageDialog(null, "تم تغيير المعلومات بنجاح");
//                    String resetid = "ALTER TABLE `add_std` DROP `id` ";
//                    String autoId = "ALTER TABLE `add_std` ADD `id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY FIRST";
//                    executeSQlQuery(resetid);
//                    executeSQlQuery(autoId);
                    this.dispose();
                    First Ft = new First();
                    Ft.setLocationRelativeTo(null);
                    Ft.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "تعذر تغيير المعلومات ");
                }
                ClearAll();

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "إن الرقم أو البريد الإلكتروني الذي أدخلته غير مقبول");
        }


    }//GEN-LAST:event_add_eleveActionPerformed

