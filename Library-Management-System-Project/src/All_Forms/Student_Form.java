    public ArrayList<books> getuserlist() {
        ArrayList<books> userList = new ArrayList<>();
        Connection connection = getConnection();
        String query = "select * from book";
        Statement st;
        ResultSet rs;
        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            books book;
            while (rs.next()) {
                book = new books(rs.getInt("book_id"), rs.getString("bookname"), rs.getInt("bookno"), rs.getDate("cr_date"), rs.getString("creator"), rs.getString("house"), rs.getString("book_spc"), rs.getBytes("book_img"), rs.getString("img_path"));
                userList.add(book);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        return userList;
    }

    public void show_information_in_JTable() {
        ArrayList<books> list = getuserlist();
        DefaultTableModel f = (DefaultTableModel) tblbook.getModel();
        Object obj[] = new Object[8];
        for (int x = 0; x < list.size(); x++) {
            //  obj[0] = list.get(x).getImage_path();
            obj[0] = list.get(x).getSpc();
            obj[1] = list.get(x).getHouse();
            obj[2] = list.get(x).getCreator();
            obj[3] = list.get(x).getCr_date();
            obj[4] = list.get(x).getBookno();
            obj[5] = list.get(x).getBookname();
            obj[6] = list.get(x).getId();

            f.addRow(obj);
        }
    }

