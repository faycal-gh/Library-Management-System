    private void tblbookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbookMouseClicked
        // TODO add your handling code here:
        int row = tblbook.getSelectedRow();
        table_clicked = (tblbook.getValueAt(row, 6).toString());
        int table_Id = Integer.parseInt(table_clicked);
        String Query = "SELECT * FROM `book` WHERE `book_id` =" + table_clicked;
        try {
            ps = getConnection().prepareStatement(Query);
            rs = ps.executeQuery();
            if (rs.next()) {
                String id = rs.getString("book_id");
                txt_book_number.setText(id);

                String book = rs.getString("bookname");
                txt_book_name.setText(book);

                String bookno = rs.getString("bookno");
                txt_book_no.setText(bookno);

                String creator = rs.getString("creator");
                txt_creator_name.setText(creator);

                String house = rs.getString("house");
                txt_house.setText(house);

                Date cr_date = rs.getDate("cr_date");
                txt_date.setDate(cr_date);

                String spc = rs.getString("book_spc");
                txt_spc.setText(spc);

             
                byte[] std_Image = rs.getBytes("book_img");
                ImageIcon imgicon = new ImageIcon(new ImageIcon(std_Image).getImage().getScaledInstance(lbl_img_book.getWidth(), lbl_img_book.getHeight(), Image.SCALE_SMOOTH));
                lbl_img_book.setIcon(imgicon);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }
//        showselectedrow();
    }//GEN-LAST:event_tblbookMouseClicked
