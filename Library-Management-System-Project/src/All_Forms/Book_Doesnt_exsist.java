    public ArrayList<Not_Exists> getuserlist() {
        ArrayList<Not_Exists> userList = new ArrayList<>();
        Connection connection = getConnection();
        String query = "select * from book , add_emprunt where bookno = add_emprunt.book_no ";
        Statement st;
        ResultSet rs;
        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            Not_Exists ne;
            while (rs.next()) {
                ne = new Not_Exists(rs.getInt("book_id"), rs.getString("bookname"), rs.getInt("bookno"), rs.getString("creator"), rs.getDate("cr_date"), rs.getString("house"), rs.getString("book_spc"), rs.getBytes("book_img"), rs.getString("img_path"));
                userList.add(ne);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        return userList;
    }

    public void show_information_in_JTable() {
        ArrayList<Not_Exists> list = getuserlist();
        DefaultTableModel f = (DefaultTableModel) book_not_exsist.getModel();
        Object obj[] = new Object[7];
        for (int x = 0; x < list.size(); x++) {
            obj[0] = list.get(x).getSpc();
            obj[1] = list.get(x).getHouse();
            obj[2] = list.get(x).getCr_date();
            obj[3] = list.get(x).getCreator();
            obj[4] = list.get(x).getBookno();
            obj[5] = list.get(x).getBookname();
            obj[6] = list.get(x).getId();
            f.addRow(obj);
        }
    }

