    private void book_not_exsistMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_book_not_exsistMouseClicked
        // TODO add your handling code here:
        int row = book_not_exsist.getSelectedRow();
        table_clicked = (book_not_exsist.getValueAt(row, 6).toString());
        int table_Id = Integer.parseInt(table_clicked);
        String Query = "SELECT * FROM `book` WHERE `book_id` =" + table_clicked;
        try {
            ps = getConnection().prepareStatement(Query);
            rs = ps.executeQuery();
            if (rs.next()) {
                String id = rs.getString("book_id");
                txt_book_number.setText(id);

                String title = rs.getString("bookname");
                txt_book_name.setText(title);

                String bookno = rs.getString("bookno");
                txt_book_no.setText(bookno);

                String creator_name = rs.getString("creator");
                txt_creator_name.setText(creator_name);

                Date cr_date = rs.getDate("cr_date");
                txt_date.setDate(cr_date);

                String bookImgPath = rs.getString("img_path");
                book_img_path1.setText(bookImgPath);

                String Dar_Nashr = rs.getString("house");
                txt_house.setText(Dar_Nashr);

                String Spc = rs.getString("book_spc");
                txt_spc.setText(Spc);

                byte[] std_Image = rs.getBytes("book_img");
                ImageIcon imgicon = new ImageIcon(new ImageIcon(std_Image).getImage().getScaledInstance(lbl_img_book1.getWidth(), lbl_img_book1.getHeight(), Image.SCALE_SMOOTH));
                lbl_img_book1.setIcon(imgicon);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }

    }//GEN-LAST:event_book_not_exsistMouseClicked

